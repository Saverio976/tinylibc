#include "tlcmaths.h"

__attribute__((const)) int x_min(int a, int b)
{
    return (X_MIN(a, b));
}

__attribute__((const)) double x_mind(double a, double b)
{
    return (X_MIN(a, b));
}

__attribute__((const)) float x_minf(float a, float b)
{
    return (X_MIN(a, b));
}

__attribute__((const)) long double x_minl(long double a,
    long double b)
{
    return (X_MIN(a, b));
}
